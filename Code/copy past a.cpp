int dp[5001];

int rec(int n)
{
    if (n == 0)
        return 0;

    if (dp[n] != -1)
        return dp[n];

    dp[n] = rec(n - 1) + 1;

    for (int i = 3; i <= n; i++)
    {
        dp[n] = max(dp[n], (i - 1) * rec(n - i));
    }

    return dp[n];
}

void solve()
{
    int n;

    n = rnd(10, 1000);

    memset(dp, -1, sizeof(dp));

    int ans = rec(n);

    int mem[n + 1] = {0};
    mem[1] = 1;

    for (int i = 1, j; i < n; i++)
    {
        mem[i + 1] = max(mem[i + 1], mem[i] + 1);
        for (j = i + 3; j <= n; j++)
            mem[j] = max(mem[j], (j - i - 1) * mem[i]);
    }

    if (ans != mem[n])
    {
        cout << n << el;
    }

    return;
}
