#include <iostream>

using namespace std ;


int main()
 {
  cout<<"Size of char: "<<sizeof(char )<<" byte"<<endl<<endl ;
  cout<<"Size of int: "<<sizeof(int )<<" bytes"<<endl<<endl ;
  cout<<"Size of float: "<<sizeof(float )<<" bytes"<<endl<<endl ;
  cout<<"Size of double: "<<sizeof(double )<<" bytes"<<endl<<endl;
  
  return 0;
 }